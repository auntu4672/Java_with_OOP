public class Assignmentoperator {
    public static void main(String[] args) {
        int x = 3;
        int y = 9;

        x += y;
        System.out.println("x=" + x);

        x -= y;
        System.out.println("x=" + x);

        x *= y;
        System.out.println("x=" + x);

        x /= y;
        System.out.println("x=" + x);

        x %= y;
        System.out.println("x=" + x);

    }

}
